def generate_go_graph(name: str) -> dict:
    safe_name = name.replace("-", "_")
    imports = '''\t"context"
\t"fmt"
\t"log/slog"
\t"os"'''

    agents = f"""type {safe_name.title()}Agent struct {{
    name string
}}

func New{safe_name.title()}Agent(name string) *{safe_name.title()}Agent {{
    return &{safe_name.title()}Agent{{name: name}}
}}

func (a *{safe_name.title()}Agent) Run(ctx context.Context, input string) (string, error) {{
    slog.Info("agent run", "agent", a.name, "input", input)
    return fmt.Sprintf("Processed: %s", input), nil
}}"""

    tools = """type ToolContext struct {
    ToolName string
}

func executeTool(ctx ToolContext, params map[string]any) (map[string]any, error) {
    slog.Info("tool executed", "tool", ctx.ToolName)
    return map[string]any{"status": "ok"}, nil
}"""

    workflow = f"""type WorkflowNode struct {{
    ID    string
    Agent *{safe_name.title()}Agent
}}

type WorkflowEdge struct {{
    Source    string
    Target    string
    Condition func(state map[string]any) bool
}}

type {safe_name.title()}Workflow struct {{
    Nodes      []WorkflowNode
    Edges      []WorkflowEdge
    EntryPoint string
    State      map[string]any
}}

func New{safe_name.title()}Workflow() *{safe_name.title()}Workflow {{
    validateAgent := New{safe_name.title()}Agent("{name}_validator")
    processAgent := New{safe_name.title()}Agent("{name}_processor")

    return &{safe_name.title()}Workflow{{
        Nodes: []WorkflowNode{{
            {{ID: "validate", Agent: validateAgent}},
            {{ID: "process", Agent: processAgent}},
        }},
        Edges: []WorkflowEdge{{
            {{Source: "validate", Target: "process"}},
        }},
        EntryPoint: "validate",
        State:      make(map[string]any),
    }}
}}

func (w *{safe_name.title()}Workflow) Run(ctx context.Context, query string) error {{
    slog.Info("workflow start", "correlation_id", fmt.Sprintf("wf_%p", w))

    currentNodeID := w.EntryPoint
    for currentNodeID != "" {{
        node := w.findNode(currentNodeID)
        if node == nil {{
            return fmt.Errorf("node not found: %s", currentNodeID)
        }}

        result, err := node.Agent.Run(ctx, query)
        if err != nil {{
            return fmt.Errorf("node %s error: %w", currentNodeID, err)
        }}

        w.State[currentNodeID+"_result"] = result
        currentNodeID = w.nextNode(currentNodeID)
    }}

    slog.Info("workflow complete")
    return nil
}}

func (w *{safe_name.title()}Workflow) findNode(id string) *WorkflowNode {{
    for i := range w.Nodes {{
        if w.Nodes[i].ID == id {{
            return &w.Nodes[i]
        }}
    }}
    return nil
}}

func (w *{safe_name.title()}Workflow) nextNode(currentID string) string {{
    for _, edge := range w.Edges {{
        if edge.Source == currentID {{
            if edge.Condition == nil || edge.Condition(w.State) {{
                return edge.Target
            }}
        }}
    }}
    return ""
}}"""

    entrypoint = f"""func main() {{
    ctx := context.Background()

    wf := New{safe_name.title()}Workflow()
    if err := wf.Run(ctx, "Test query"); err != nil {{
        slog.Error("workflow failed", "error", err)
        os.Exit(1)
    }}

    fmt.Println("Workflow completed successfully")
}}"""

    return {"imports": imports, "agents": agents, "tools": tools, "workflow": workflow, "entrypoint": entrypoint}


