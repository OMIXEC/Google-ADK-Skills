def main():
    all_wf_types = list(WORKFLOW_TYPES.keys())
    # TS has its own types
    ts_types = list(TS_GENERATORS.keys())

    parser = argparse.ArgumentParser(
        description="Scaffold a new ADK agentic workflow project",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="Examples:\n"
               "  python init_adk_workflow.py --type graph --lang python --name my-flow\n"
               "  python init_adk_workflow.py --type collaborative --lang python --name support-triage\n"
               "  python init_adk_workflow.py --type dynamic --lang go --name order-pipeline\n"
               "  python init_adk_workflow.py --type graph --lang ts --name ts-workflow\n"
               "  python init_adk_workflow.py --type agent-server --lang ts --name my-server\n"
               "  python init_adk_workflow.py --type template-loop --lang python --name refinement-loop --with-mcp",
    )
    parser.add_argument(
        "--type", required=True,
        choices=all_wf_types + ts_types,
        help="Workflow type"
    )
    parser.add_argument(
        "--lang", required=True,
        choices=["python", "go", "ts"],
        help="Programming language"
    )
    parser.add_argument(
        "--name", required=True,
        help="Project/package name (kebab-case recommended)"
    )
    parser.add_argument(
        "--output-dir",
        help="Parent directory for the project (default: current directory)"
    )
    parser.add_argument(
        "--with-mcp", action="store_true",
        help="Add MCP server scaffolding and tool examples"
    )
    parser.add_argument(
        "--with-memory", action="store_true",
        help="Add SessionService configuration and memory hierarchy setup"
    )
    parser.add_argument(
        "--with-model-armor", action="store_true",
        help="Add Model Armor content safety: double-shield runner, template configs, security plugin"
    )

    args = parser.parse_args()

    # Validate type/language combinations
    if args.lang == "go" and args.type not in GO_GENERATORS:
        print(f"Error: Go does not support '{args.type}' workflow type")
        print(f"  Go supported types: {list(GO_GENERATORS.keys())}")
        sys.exit(1)

    if args.lang == "ts" and args.type not in TS_GENERATORS:
        print(f"Error: TypeScript does not support '{args.type}' workflow type")
        print(f"  TS supported types: {list(TS_GENERATORS.keys())}")
        sys.exit(1)

    scaffold_project(args.name, args.type, args.lang, args.output_dir,
                     with_mcp=args.with_mcp, with_memory=args.with_memory,
                     with_model_armor=args.with_model_armor)


if __name__ == "__main__":
    main()
